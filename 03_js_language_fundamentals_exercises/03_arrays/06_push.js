function push(arr){
  for (i = 1; i < arguments.length; i++) {
    arr[arr.length] = arguments[i]
  }
  return arr.length
}

const array2 = [1, 2, 3];
console.log(push(array2, 4, 5, 6));              // 6
console.log(array2);                // [1, 2, 3, 4, 5, 6]
console.log(push([1, 2], ['a', 'b']));          // 3
console.log(push([], 1));                       // 1
console.log(push([]));                          // 0